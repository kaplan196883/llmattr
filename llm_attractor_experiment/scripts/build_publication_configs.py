"""
Generate the three publication-grade configs (O1_pub, O2_pub, D1_pub) by
inlining families from configs/prompt_library_publication.yaml into
lean templates. Run once before launching the publication experiments.

Usage:
    python -m scripts.build_publication_configs
    # ... later ...
    python -u -m src.experiments.operators.main all --config configs/operators/O1_pub.yaml
    python -u -m src.experiments.operators.main all --config configs/operators/O2_pub.yaml
    python -u -m src.experiments.dialog.main all    --config configs/dialog/D1_pub.yaml
"""
from __future__ import annotations

import sys
from pathlib import Path

import yaml

ROOT = Path(__file__).resolve().parent.parent
LIBRARY_PATH = ROOT / "configs" / "prompt_library_publication.yaml"


def load_library() -> dict[str, list[str]]:
    raw = yaml.safe_load(LIBRARY_PATH.read_text(encoding="utf-8"))
    return {name: fam["seeds"] for name, fam in raw["families"].items()}


# ---- shared analysis block reused across all 3 configs -----------------------

ANALYSIS_BLOCK = {
    "pca_dims": [2, 10, 20],
    "clustering": {
        "method": "kmeans",
        "dbscan": {"eps": 0.08, "min_samples": 4},
        "kmeans": {"n_clusters": 12},  # bumped to 12 for richer family × seed resolution
    },
    "recurrence": {"metric": "cosine", "epsilon": 0.15, "tau": 3},
    "dwell": {"space": "pca10"},
    "tsne": {
        "enabled": True,
        "perplexity": 30,
        "pre_pca_dim": 50,
        "metric": "cosine",
        "include_in_metrics": False,
        "recurrence_epsilon_std_frac": 0.08,
        "cluster_in_tsne": False,
    },
    "basin": {
        "target_region_late_fraction": 0.3,
        "target_step_fraction": 0.7,
        "perturbations": ["seed_only"],
    },
    "basin_entry": {"enabled": True, "late_fraction": 0.3, "fraction_after": 0.7},
    "late_recurrence": {"enabled": True, "start_fraction": 0.5, "use_basin_entry": True},
    "exit_return": {"enabled": True, "start_fraction": 0.0, "use_basin_entry": True},
    "bootstrap": {"enabled": True, "n_resamples": 1000, "confidence": 0.95},
}


def make_family_block(library: dict, family_names: list[str], system_prompt: str) -> list[dict]:
    blocks = []
    for name in family_names:
        blocks.append(
            {
                "name": name,
                "system_prompt": system_prompt,
                "initial_conditions": list(library[name]),
            }
        )
    return blocks


def write_yaml(path: Path, header_comment: str, data: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    body = yaml.safe_dump(data, sort_keys=False, allow_unicode=True, width=140)
    text = f"# {header_comment}\n# AUTO-GENERATED by scripts/build_publication_configs.py — edit the builder, not this file.\n\n{body}"
    path.write_text(text, encoding="utf-8")
    print(f"wrote {path} ({len(body.splitlines())} lines)")


# ---- O1 continue/append ------------------------------------------------------


def build_o1_continue(library: dict) -> dict:
    system_prompt = "Continue the text naturally. Do not summarize or explain."
    all_families = list(library.keys())
    cfg = {
        "experiment_id": "exp_pub_O1_continue",
        "generation_model": "gpt-4o-mini",
        "embedding_model": "text-embedding-3-small",
        "steps_per_run": 40,
        "runs_per_condition": 3,
        "initial_conditions_per_family": 30,
        "max_output_tokens": 120,
        "temperature": 0.8,
        "top_p": 1.0,
        "include_logprobs": False,
        "clip_rule": "tail_chars",
        "max_context_chars": 12000,
        "loop_mode": "append",
        "rolling_window_k": 3,
        "context_tail_chars": 4000,
        "context_full_chars": 8000,
        "observables": ["output", "rolling_k3", "context_tail"],
        **ANALYSIS_BLOCK,
        "baseline_modes": ["no_feedback", "time_shuffled"],
        "batch_embeddings": False,
        "use_evals": False,
        "seed": 42,
        "parallel_trajectories": 8,
        "output_dir": "data",
        "prompt_families": make_family_block(library, all_families, system_prompt),
    }
    return cfg


