# ---- D1 dialog (curious user + helpful agent) --------------------------------


def build_d1_dialog(library: dict) -> dict:
    # Dialog uses conversational-style families. The family-level system_prompt
    # is "(unused)" because dialog uses dialog.role_a / role_b system_prompts.
    dialog_families = [
        "philosophy_dialog",
        "practical_dialog",
        "creative_dialog",
        "reflective",
        "emotional",
    ]
    cfg = {
        "experiment_id": "exp_pub_D1_dialog_curious_helpful",
        "generation_model": "gpt-4o-mini",
        "embedding_model": "text-embedding-3-small",
        "steps_per_run": 20,  # 10 turns per role
        "runs_per_condition": 3,
        "initial_conditions_per_family": 30,
        "max_output_tokens": 140,
        "temperature": 0.8,
        "top_p": 1.0,
        "include_logprobs": False,
        "clip_rule": "tail_chars",
        "max_context_chars": 12000,
        "loop_mode": "append",
        "rolling_window_k": 3,
        "context_tail_chars": 4000,
        "context_full_chars": 8000,
        "observables": [
            "output",
            "rolling_k3",
            "context_tail",
            "last_user_turn",
            "last_agent_turn",
            "rolling_user_k3",
            "rolling_agent_k3",
            "turn_pair",
        ],
        **ANALYSIS_BLOCK,
        "baseline_modes": ["time_shuffled"],  # dialog has no meaningful no_feedback
        "batch_embeddings": False,
        "use_evals": False,
        "seed": 42,
        "parallel_trajectories": 8,
        "output_dir": "data",
        "dialog": {
            "role_a": {
                "name": "user",
                "system_prompt": (
                    "You are roleplaying as a curious user in an ongoing conversation. "
                    "Read the transcript and respond naturally as the User, asking a "
                    "follow-up question or sharing a related thought based on the Agent's "
                    "last message. Keep responses to 1-2 sentences. Do not restate the "
                    "whole conversation."
                ),
            },
            "role_b": {
                "name": "agent",
                "system_prompt": (
                    "You are a thoughtful assistant in an ongoing conversation. Read the "
                    "transcript and respond naturally as the Agent to the User's last "
                    "message. Keep responses to 1-2 sentences. Do not restate the whole "
                    "conversation."
                ),
            },
            "initiator": "role_b",
        },
        "prompt_families": make_family_block(
            library, dialog_families, system_prompt="(unused in dialog mode)"
        ),
    }
    return cfg


# ---- main --------------------------------------------------------------------


def main(argv: list[str] | None = None) -> int:
    library = load_library()
    total_seeds = sum(len(v) for v in library.values())
    print(f"loaded library: {len(library)} families, {total_seeds} seeds")

    o1 = build_o1_continue(library)
    o2 = build_o2_paraphrase_replace(library)
    d1 = build_d1_dialog(library)

    write_yaml(
        ROOT / "configs" / "operators" / "O1_pub.yaml",
        "Publication-grade O1: continue / append. 15 families × 30 ICs × 3 runs × 40 steps.",
        o1,
    )
    write_yaml(
        ROOT / "configs" / "operators" / "O2_pub.yaml",
        "Publication-grade O2: paraphrase / replace. 15 families × 30 ICs × 3 runs × 40 steps.",
        o2,
    )
    write_yaml(
        ROOT / "configs" / "dialog" / "D1_pub.yaml",
        "Publication-grade D1: curious user + helpful agent dialog. 5 dialog-appropriate families × 30 ICs × 3 runs × 20 steps.",
        d1,
    )

    print("\nplanned trajectory counts:")
    print(f"  O1: 15 × 30 × 3 = {15*30*3} recursive + {15*30*3} no_feedback = {15*30*3*2} trajectories")
    print(f"  O2: 15 × 30 × 3 = {15*30*3} recursive + {15*30*3} no_feedback = {15*30*3*2} trajectories")
    print(f"  D1: 5 × 30 × 3 = {5*30*3} recursive (dialog, no no_feedback)")
    print()
    print("total generation calls (40 for O1/O2, 20 for D1):")
    o1_calls = 15 * 30 * 3 * 2 * 40
    o2_calls = 15 * 30 * 3 * 2 * 40
    d1_calls = 5 * 30 * 3 * 20
    total = o1_calls + o2_calls + d1_calls
    print(f"  O1: {o1_calls:>8} calls")
    print(f"  O2: {o2_calls:>8} calls")
    print(f"  D1: {d1_calls:>8} calls")
    print(f"  total: {total:,} calls")
    print(f"\napprox cost @ gpt-4o-mini ($0.15/M in, $0.60/M out, ~2k tokens in + 150 out avg):")
    in_cost = total * 2000 * 0.15 / 1_000_000
    out_cost = total * 150 * 0.60 / 1_000_000
    print(f"  input  tokens ≈ ${in_cost:.2f}")
    print(f"  output tokens ≈ ${out_cost:.2f}")
    print(f"  subtotal gen ≈ ${in_cost + out_cost:.2f}")
    # embedding: 3 experiments × n_observables × n_points × ~2k tokens per input
    # rough: 230K points × 3 observables × 2k = 1.4B tokens @ $0.02/M = $28 for text-embedding-3-small
    # but batching 128/call keeps this cheap
    embed_cost = 230_000 * 3 * 2000 * 0.02 / 1_000_000
    print(f"  embedding (rough upper bound) ≈ ${embed_cost:.2f}")
    print(f"  TOTAL estimated ≈ ${in_cost + out_cost + embed_cost:.2f}")
    print()
    print("wall time: rate-limited to ~500 RPM for gpt-4o-mini tier 2.")
    print(f"  {total:,} calls / 500 RPM = {total/500:.0f} min ≈ {total/500/60:.1f} hours (best case)")
    return 0


if __name__ == "__main__":
    sys.exit(main())
