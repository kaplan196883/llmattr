"""
Generate cross-model configs for every existing experiment.

For each `data/exp_*/config.yaml` we produce N copies under
`configs/cross_model/<model_tag>/<original_name>.yaml`, each with:
  - new `experiment_id` = original + "_" + model_tag
  - new `generation_model` and `generation_provider` block
  - everything else identical (so the only changing variable is the
    generation model — see ARTICLE.md §10.1).

Targets are hardcoded in TARGETS below — edit there to add/remove.
"""
from __future__ import annotations

from pathlib import Path

import yaml

REPO = Path(__file__).resolve().parent.parent
DATA = REPO / "data"
OUT_ROOT = REPO / "configs" / "cross_model"

TARGETS = {
    # tag -> (model name, provider block)
    "gpt4nano": (
        "gpt-4.1-nano",
        {
            "name": "openai",
            "api": "responses",
            "api_key_env": "OPENAI_API_KEY",
        },
    ),
    "m2_7": (
        "MiniMax-M2.7",
        {
            "name": "minimax",
            "base_url": "https://api.minimax.io/v1",
            "api_key_env": "MINIMAX_API_KEY",
            "api": "chat_completions",
            # Monthly Max plan: 15K req / 5hr = 50/min. Use 40 to
            # stay safely under and absorb burst-time variance from
            # parallel workers.
            "requests_per_minute": 40,
        },
    ),
}


def emit_one(src_cfg_path: Path, tag: str, model: str, provider: dict,
             out_root: Path) -> Path | None:
    with src_cfg_path.open(encoding="utf-8") as f:
        cfg = yaml.safe_load(f)
    if not cfg:
        return None
    orig_id = cfg.get("experiment_id")
    if not orig_id:
        return None
    new_id = f"{orig_id}_{tag}"
    cfg = dict(cfg)  # shallow copy
    cfg["experiment_id"] = new_id
    cfg["generation_model"] = model
    cfg["generation_provider"] = dict(provider)
    # Strip absolute output_dir if any — keep "data" so cross-model
    # outputs land beside baselines.
    cfg.setdefault("output_dir", "data")
    out_dir = out_root / tag
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / (src_cfg_path.parent.name + ".yaml")
    with out_path.open("w", encoding="utf-8") as f:
        f.write(f"# Cross-model: {orig_id} -> {model} ({tag})\n")
        f.write("# AUTO-GENERATED by scripts/build_cross_model_configs.py\n\n")
        yaml.safe_dump(cfg, f, sort_keys=False, allow_unicode=True)
    return out_path


def main() -> int:
    src_dirs = sorted(p for p in DATA.glob("exp_*") if p.is_dir() and (p / "config.yaml").exists())
    print(f"found {len(src_dirs)} source experiments under data/")
    n_emitted = 0
    for src in src_dirs:
        for tag, (model, provider) in TARGETS.items():
            out = emit_one(src / "config.yaml", tag, model, provider, OUT_ROOT)
            if out is not None:
                n_emitted += 1
    print(f"wrote {n_emitted} configs under {OUT_ROOT}/")
    for tag in TARGETS:
        n = len(list((OUT_ROOT / tag).glob("*.yaml")))
        print(f"  {tag}: {n} configs")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
